import { useState, useEffect } from "react";
import { ToastContainer, toast } from "react-toastify";
import "react-toastify/dist/ReactToastify.css";

export default function AdminRegister() {
  const [activeTab, setActiveTab] = useState("listar");

  const [username, setUsername] = useState("");
  const [name, setName] = useState("");
  const [role, setRole] = useState("orders");
  const [password, setPassword] = useState("");

  const [usuarios, setUsuarios] = useState([]);

  // Estado para editar
  const [editId, setEditId] = useState(null);

  useEffect(() => {
    if (activeTab === "listar") {
      carregarUsuarios();
    }
  }, [activeTab]);

  const carregarUsuarios = async () => {
    try {
      const res = await fetch("/php/employee/employees.php");
      const data = await res.json();
      setUsuarios(data);
    } catch (error) {
      toast.error("Erro ao carregar funcion치rios");
      console.error("Erro ao carregar funcion치rios:", error);
    }
  };

  const traduzRole = (role) => {
    switch (role) {
      case "administrator":
        return "Administrador";
      case "orders":
        return "Pedidos";
      case "checkin":
        return "Portaria";
      default:
        return "Outro";
    }
  };

  const handleDelete = async (id) => {
    if (window.confirm("Tem certeza que deseja excluir esse funcion치rio?")) {
      try {
        const res = await fetch("/php/employee/employees.php", {
          method: "DELETE",
          headers: { "Content-Type": "application/json" },
          body: JSON.stringify({ id }),
        });

        const data = await res.json();
        if (data.success) {
          toast.success("Funcion치rio exclu칤do com sucesso!");
          carregarUsuarios();
        } else {
          toast.error(data.message || "Erro ao excluir.");
        }
      } catch (error) {
        toast.error("Erro ao excluir funcion치rio.");
        console.error("Erro ao excluir:", error);
      }
    }
  };

  const handleSubmit = async (e) => {
    e.preventDefault();

    if (!username || !name || !password) {
      toast.warn("Preencha todos os campos.");
      return;
    }

    try {
      const res = await fetch("/php/employee/employees.php", {
        method: "POST",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify({ username, name, role, password }),
      });

      const data = await res.json();

      if (data.success) {
        toast.success("游꿀 Funcion치rio cadastrado com sucesso!");
        setUsername("");
        setName("");
        setPassword("");
        setRole("orders");
        if (activeTab === "listar") carregarUsuarios();
      } else {
        toast.error(data.message || "Erro ao cadastrar.");
      }
    } catch (error) {
      toast.error("Erro de conex칚o.");
    }
  };

  // *** Fun칞칚o para preparar edi칞칚o ***
  const handleEditClick = (user) => {
    setEditId(user.id);
    setUsername(user.username);
    setName(user.name);
    setRole(user.role);
    setPassword(""); // senha em branco, s칩 ser치 alterada se preencher
    setActiveTab("editar");
  };

  // *** Submiss칚o edi칞칚o ***
  const handleEditSubmit = async (e) => {
    e.preventDefault();

    if (!username || !name) {
      toast.warn("Usu치rio e nome s칚o obrigat칩rios.");
      return;
    }

    try {
      // Monta o payload
      const payload = { id: editId, username, name, role };
      // S칩 envia senha se preenchida
      if (password) payload.password = password;

      const res = await fetch("/php/employee/employees.php", {
        method: "PUT",
        headers: { "Content-Type": "application/json" },
        body: JSON.stringify(payload),
      });

      const data = await res.json();

      if (data.success) {
        toast.success("游꿀 Funcion치rio atualizado com sucesso!");
        setEditId(null);
        setUsername("");
        setName("");
        setPassword("");
        setRole("orders");
        setActiveTab("listar");
        carregarUsuarios();
      } else {
        toast.error(data.message || "Erro ao atualizar.");
      }
    } catch (error) {
      toast.error("Erro de conex칚o.");
    }
  };

  return (
    <div className="max-w-5xl mx-auto bg-gray-900 p-8 rounded-lg text-white">
      <ToastContainer position="top-right" autoClose={3000} />

      <h1 className="text-3xl font-bold mb-6 text-center">Gerenciar Funcion치rios</h1>

      {/* Tabs */}
      <div className="flex mb-6 border border-purple-500 rounded-lg overflow-hidden">
        <button
          onClick={() => setActiveTab("listar")}
          className={`flex-1 py-3 font-semibold text-center transition ${
            activeTab === "listar"
              ? "bg-purple-500 text-white"
              : "bg-gray-800 text-purple-400 hover:bg-purple-600 hover:text-white"
          }`}
        >
          Funcion치rios
        </button>
        <button
          onClick={() => setActiveTab("registrar")}
          className={`flex-1 py-3 font-semibold text-center transition ${
            activeTab === "registrar"
              ? "bg-purple-500 text-white"
              : "bg-gray-800 text-purple-400 hover:bg-purple-600 hover:text-white"
          }`}
        >
          Cadastrar
        </button>
        <button
          onClick={() => {
            if (!editId) {
              toast.info("Selecione um funcion치rio para editar clicando em 'Editar' na lista.");
              setActiveTab("listar");
            } else {
              setActiveTab("editar");
            }
          }}
          className={`flex-1 py-3 font-semibold text-center transition ${
            activeTab === "editar"
              ? "bg-purple-500 text-white"
              : "bg-gray-800 text-purple-400 hover:bg-purple-600 hover:text-white"
          }`}
        >
          Editar
        </button>
      </div>

      {/* Conte칰do */}
      {activeTab === "listar" && (
        <div className="overflow-x-auto rounded-lg shadow mb-10">
          <table className="min-w-full text-white border border-gray-700 rounded overflow-hidden">
            <thead className="bg-gray-800 text-white">
              <tr>
                <th className="px-4 py-3 text-left">ID</th>
                <th className="px-4 py-3 text-left">Usu치rio</th>
                <th className="px-4 py-3 text-left">Nome Completo</th>
                <th className="px-4 py-3 text-left">Fun칞칚o</th>
                <th className="px-4 py-3 text-left">A칞칫es</th>
              </tr>
            </thead>
            <tbody>
              {usuarios.map((user) => (
                <tr
                  key={user.id}
                  className="border-t border-gray-700 hover:bg-gray-800"
                >
                  <td className="px-4 py-2">{user.id}</td>
                  <td className="px-4 py-2">{user.username}</td>
                  <td className="px-4 py-2">{user.name}</td>
                  <td className="px-4 py-2">{traduzRole(user.role)}</td>
                  <td className="px-4 py-2 space-x-2">
                    <button
                      onClick={() => handleEditClick(user)}
                      className="bg-blue-600 hover:bg-blue-700 text-white px-3 py-1 rounded font-semibold text-sm"
                    >
                      Editar
                    </button>
                    <button
                      onClick={() => handleDelete(user.id)}
                      className="bg-red-600 hover:bg-red-700 text-white px-3 py-1 rounded font-semibold text-sm"
                    >
                      Excluir
                    </button>
                  </td>
                </tr>
              ))}
            </tbody>
          </table>
        </div>
      )}

      {activeTab === "registrar" && (
        <div>
          <h2 className="text-2xl mb-4 font-bold text-center">
            Cadastrar Novo Funcion치rio
          </h2>

          <form onSubmit={handleSubmit} className="flex flex-col gap-4 max-w-md mx-auto">
            <input
              type="text"
              placeholder="Usu치rio (username)"
              value={username}
              onChange={(e) => setUsername(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-green-600"
              required
            />
            <input
              type="text"
              placeholder="Nome completo"
              value={name}
              onChange={(e) => setName(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-green-600"
              required
            />
            <input
              type="password"
              placeholder="Senha"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-green-600"
              required
            />
            <select
              value={role}
              onChange={(e) => setRole(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-green-600"
            >
              <option value="administrator">Administrador</option>
              <option value="orders">Pedidos</option>
              <option value="checkin">Portaria</option>
            </select>
            <button
              type="submit"
              className="bg-green-600 py-3 rounded font-semibold hover:bg-green-700 transition"
            >
              Cadastrar
            </button>
          </form>
        </div>
      )}

      {activeTab === "editar" && (
        <div>
          <h2 className="text-2xl mb-4 font-bold text-center">Editar Funcion치rio</h2>

          <form onSubmit={handleEditSubmit} className="flex flex-col gap-4 max-w-md mx-auto">
            <input
              type="text"
              placeholder="Usu치rio (username)"
              value={username}
              onChange={(e) => setUsername(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-yellow-500"
              required
            />
            <input
              type="text"
              placeholder="Nome completo"
              value={name}
              onChange={(e) => setName(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-yellow-500"
              required
            />
            <input
              type="password"
              placeholder="Senha (deixe vazio para n칚o alterar)"
              value={password}
              onChange={(e) => setPassword(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-yellow-500"
            />
            <select
              value={role}
              onChange={(e) => setRole(e.target.value)}
              className="p-3 rounded bg-gray-800 border border-gray-700 focus:ring-2 focus:ring-yellow-500"
            >
              <option value="administrator">Administrador</option>
              <option value="orders">Pedidos</option>
              <option value="checkin">Portaria</option>
            </select>
            <div className="flex justify-center gap-4">
              <button
                type="submit"
                className="bg-yellow-600 py-3 rounded font-semibold hover:bg-yellow-700 transition w-32"
              >
                Atualizar
              </button>
              <button
                type="button"
                onClick={() => {
                  setActiveTab("listar");
                  setEditId(null);
                  setUsername("");
                  setName("");
                  setPassword("");
                  setRole("orders");
                }}
                className="bg-gray-600 py-3 rounded font-semibold hover:bg-gray-700 transition w-32"
              >
                Cancelar
              </button>
            </div>
          </form>
        </div>
      )}
    </div>
  );
}
